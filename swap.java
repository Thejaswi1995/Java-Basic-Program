package com.BridgelabzBasicCore;

public class swap {
        public static void main(String[] args)
        {
            int a = 10;
            int b = 20;
            int temp = 0;
            System.out.println("The actual of a =" +a);
            System.out.println("The actual of b =" +b);
            System.out.println("After swaping of two numbers");
            temp = a;
            a = b;
            b = temp;
            System.out.println("The present value of a =" +a);
            System.out.println("The present value of b =" +b);
        }
    }
